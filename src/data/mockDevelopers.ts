export function getDeveloperById(id: string): Developer | undefined {
  return mockDevelopers.find((d) => d.id === id);
}

export function getDeveloperByUsername(username: string): Developer | undefined {
  return mockDevelopers.find((d) => d.username.toLowerCase() === username.toLowerCase());
}

export function searchDevelopers(query: string): Developer[] {
  const q = query.trim().toLowerCase();
  if (!q) return [];
  return mockDevelopers.filter(
    (d) =>
      d.username.toLowerCase().includes(q) ||
      d.displayName.toLowerCase().includes(q)
  ).slice(0, 8);
}

export function normalizeContributions(contributions: number, min: number, max: number): number {
  if (max === min) return 1;
  return 0.5 + ((contributions - min) / (max - min)) * 3.5;
}

export function normalizeRepoCount(repoCount: number, min: number, max: number): number {
  if (max === min) return 1;
  return 0.6 + ((repoCount - min) / (max - min)) * 1.4;
}

export function getContributionRange(): { min: number; max: number } {
  const values = mockDevelopers.map((d) => d.contributions);
  return { min: Math.min(...values), max: Math.max(...values) };
}

export function getRepoCountRange(): { min: number; max: number } {
  const values = mockDevelopers.map((d) => d.repoCount);
  return { min: Math.min(...values), max: Math.max(...values) };
}
